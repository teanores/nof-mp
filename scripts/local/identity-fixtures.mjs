async function seed({ reset = false } = {}) {
  assertLocalDatabaseUrl(localDatabaseUrl);
  const client = new Client({ connectionString: localDatabaseUrl });
  await client.connect();
  try {
    await client.query("BEGIN");
    await client.query('CREATE SCHEMA IF NOT EXISTS dragon_forge');
    await client.query('CREATE EXTENSION IF NOT EXISTS "pgcrypto"');
    await client.query(`
      CREATE TABLE IF NOT EXISTS dragon_forge.rank (
        id integer PRIMARY KEY,
        rank_id integer UNIQUE NOT NULL,
        rank_name varchar(64) UNIQUE NOT NULL
      )`);
    await client.query(`
      CREATE TABLE IF NOT EXISTS dragon_forge.level (
        id integer PRIMARY KEY,
        level_id integer UNIQUE NOT NULL,
        level_name varchar(64) UNIQUE NOT NULL,
        min_experience integer NOT NULL,
        max_experience integer NOT NULL,
        rank_id integer NULL
      )`);
    await client.query(`
      CREATE TABLE IF NOT EXISTS dragon_forge.role (
        id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name varchar(32) UNIQUE NOT NULL,
        display_name varchar(64) NOT NULL,
        description varchar(256),
        sort_order integer NOT NULL DEFAULT 0
      )`);
    await client.query(`
      CREATE TABLE IF NOT EXISTS dragon_forge."user" (
        id uuid PRIMARY KEY,
        username varchar(50) UNIQUE NOT NULL,
        email varchar(120) UNIQUE NOT NULL,
        password_hash varchar(256),
        token varchar(32),
        token_expiration timestamp,
        experience integer DEFAULT 0,
        level_id integer,
        rank_id integer,
        about_me varchar(140),
        last_seen timestamp,
        telegram_id bigint UNIQUE,
        telegram_username varchar(50),
        telegram_lastname varchar(50),
        telegram_firstname varchar(50),
        telegram_language_code varchar(16),
        registration_source varchar(32),
        user_created_at timestamp with time zone DEFAULT now(),
        role_id integer REFERENCES dragon_forge.role(id) ON DELETE SET NULL
      )`);

    if (reset) {
      await client.query(`DELETE FROM dragon_forge."user" WHERE email LIKE '%.local@forgath.test'`);
    }

    await client.query(`
      INSERT INTO dragon_forge.rank (id, rank_id, rank_name)
      VALUES (1, 1, 'Странник')
      ON CONFLICT (id) DO UPDATE SET rank_id = EXCLUDED.rank_id, rank_name = EXCLUDED.rank_name`);
    await client.query(`
      INSERT INTO dragon_forge.level (id, level_id, level_name, min_experience, max_experience, rank_id)
      VALUES (1, 1, 'Новобранец', 0, 99, NULL)
      ON CONFLICT (id) DO UPDATE SET level_id = EXCLUDED.level_id, level_name = EXCLUDED.level_name`);
    await client.query(`
      INSERT INTO dragon_forge.role (name, display_name, description, sort_order)
      VALUES
        ('admin', 'Администратор', 'Local test admin', 1),
        ('moderator', 'Модератор', 'Local test moderator', 2),
        ('user', 'Участник', 'Local test user', 3)
      ON CONFLICT (name) DO UPDATE
      SET display_name = EXCLUDED.display_name, description = EXCLUDED.description, sort_order = EXCLUDED.sort_order`);

    for (const user of users) {
      const passwordHash = user.password ? hashPassword(user.password) : null;
      await client.query(
        `
        INSERT INTO dragon_forge."user" (
          id, username, email, password_hash, experience, level_id, rank_id, about_me,
          last_seen, registration_source, user_created_at, role_id
        )
        SELECT $1::uuid, $2, $3, $4, 0, 1, 1, $5, now(), 'local_seed', now(), role.id
        FROM dragon_forge.role role
        WHERE role.name = $6
        ON CONFLICT (id) DO UPDATE SET
          username = EXCLUDED.username,
          email = EXCLUDED.email,
          password_hash = EXCLUDED.password_hash,
          role_id = EXCLUDED.role_id,
          registration_source = EXCLUDED.registration_source`,
        [user.id, user.username, user.email, passwordHash, `Local ${user.role} fixture`, user.role],
      );
    }

    await client.query("COMMIT");
    console.log(`Seeded ${users.length} local identity users into ${new URL(localDatabaseUrl).pathname.slice(1)}.`);
  } catch (error) {
    await client.query("ROLLBACK");
    throw error;
  } finally {
    await client.end();
  }
}

async function bootstrapDatabase() {
  assertLocalHost(localPostgresAdminUrl);
  const adminUrl = new URL(localPostgresAdminUrl);
  const adminClient = new Client({ connectionString: localPostgresAdminUrl });
  await adminClient.connect();
  try {
    const roleExists = await adminClient.query("SELECT 1 FROM pg_roles WHERE rolname = 'nof_local'");
    if (roleExists.rowCount === 0) {
      await adminClient.query("CREATE ROLE nof_local LOGIN PASSWORD 'nof_local'");
    }

    const databaseExists = await adminClient.query("SELECT 1 FROM pg_database WHERE datname = 'nof_local'");
    if (databaseExists.rowCount === 0) {
      await adminClient.query("CREATE DATABASE nof_local OWNER nof_local");
    }

    console.log(`Prepared local database nof_local through ${adminUrl.hostname}.`);
  } finally {
    await adminClient.end();
  }
}

