function passlibBase64Encode(value) {
  return value.toString("base64").replace(/\+/g, ".").replace(/=+$/g, "");
}

function hashPassword(password) {
  const salt = crypto.randomBytes(16);
  const digest = crypto.pbkdf2Sync(password, salt, 29000, 32, "sha256");
  return `$pbkdf2-sha256$29000$${passlibBase64Encode(salt)}$${passlibBase64Encode(digest)}`;
}

function assertLocalDatabaseUrl(value) {
  if (!value) {
    throw new Error("NOF_LOCAL_DATABASE_URL is required for database actions.");
  }
  assertLocalHost(value);
  const url = new URL(value);
  const databaseName = url.pathname.replace(/^\//, "").toLowerCase();
  if (!/(nof|forgath).*(local|test)|(local|test).*(nof|forgath)/.test(databaseName)) {
    throw new Error(`Refusing database name without local/test marker: ${databaseName}`);
  }
}

function assertLocalHost(value) {
  if (!value) {
    throw new Error("A local PostgreSQL connection URL is required.");
  }
  const url = new URL(value);
  const allowedHosts = new Set(["localhost", "127.0.0.1", "::1"]);
  if (!allowedHosts.has(url.hostname)) {
    throw new Error(`Refusing non-local database host: ${url.hostname}`);
  }
}

